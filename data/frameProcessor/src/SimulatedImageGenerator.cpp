SimulatedImageGenerator::SimulatedImageGenerator(int width, int height, int bitDepth, ImageFormat format)
    : width_(width), height_(height), bitDepth_(bitDepth), format_(format), current_frame_index_(0) {
    current_frame_.resize(width * height);
	loadImagesFromFile("/data/odinlogo.bin");
}


// Load images from .bin file, load them all into memory for faster access
void SimulatedImageGenerator::loadImagesFromFile(const std::string& filePath) {
    std::ifstream file(filePath, std::ios::binary);
    if (!file) {
        throw std::runtime_error("Failed to open file: " + filePath);
    }

    uint32_t storedImageWidth, storedImageHeight;
    file.read(reinterpret_cast<char*>(&storedImageWidth), sizeof(storedImageWidth));
    file.read(reinterpret_cast<char*>(&storedImageHeight), sizeof(storedImageHeight));

    if (storedImageWidth != static_cast<uint32_t>(width_) || storedImageHeight != static_cast<uint32_t>(height_)) {
        throw std::runtime_error("Image dimensions in file do not match expected dimensions");
    }

    size_t frame_size = width_ * height_;
    std::vector<uint16_t> frame_buffer(frame_size);

	// Clear currectly loaded frames to allow user to change images
    loaded_frames_.clear();
    while (file.read(reinterpret_cast<char*>(frame_buffer.data()), frame_size * sizeof(uint16_t))) {
        loaded_frames_.push_back(frame_buffer);
    }

	// Check if loading the frames failed.
    if (loaded_frames_.empty()) {
        throw std::runtime_error("No frames loaded from file");
    }

    std::cout << "Loaded " << loaded_frames_.size() << " frames from file." << std::endl;
}

void SimulatedImageGenerator::generateImage(int cameraNumber, uint64_t frameNumber) {
    if (loaded_frames_.empty()) {
        throw std::runtime_error("No frames loaded. Call loadImagesFromFile() first.");
    }

    current_frame_ = loaded_frames_[current_frame_index_];
    current_frame_index_ = (current_frame_index_ + 1) % loaded_frames_.size();

    drawText("CAMERA: " + std::to_string(cameraNumber), 10, 30, 0xFFFF, 4);
    drawText("FRAME: " + std::to_string(frameNumber), 10, 240, 0xFFFF, 4);
}

const std::vector<uint16_t>& SimulatedImageGenerator::getImageData() const {
    return current_frame_;
}

size_t SimulatedImageGenerator::getFrameCount() const {
    return loaded_frames_.size();
}

void SimulatedImageGenerator::drawPixel(int x, int y, uint16_t value) {
    if (x >= 0 && x < width_ && y >= 0 && y < height_) {
        current_frame_[y * width_ + x] = value;
    }
}

void SimulatedImageGenerator::drawChar(char c, int x, int y, uint16_t value, int scale) {
    int index = (c - 32) * 120; // Calculate starting index (assuming ' ' starts at ASCII 32)
    int charWidth = 24;
    int charHeight = 40;

    for (int row = 0; row < charHeight; ++row) {
        for (int col = 0; col < charWidth; col += 8) { // Each byte represents 8 pixels
            uint8_t byte = Font40_Table[index + (row * 3) + (col / 8)];
            for (int bit = 0; bit < 8; ++bit) {
                if (byte & (0x80 >> bit)) { // Check each bit in the byte
                    // Draw a block of pixels for each bit set, size depending on 'scale'
                    for (int dy = 0; dy < scale; dy++) {
                        for (int dx = 0; dx < scale; dx++) {
                            drawPixel(x + (col + bit) * scale + dx, y + row * scale + dy, value);
                        }
                    }
                }
            }
        }
    }
}

void SimulatedImageGenerator::drawText(const std::string& text, int x, int y, uint16_t value, int scale) {
    for (size_t i = 0; i < text.length(); ++i) {
        drawChar(text[i], x + i * 24 * scale, y, value, scale); // Adjust spacing for 24 pixel width characters
    }
}

